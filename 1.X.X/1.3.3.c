#include <stdio.h>
#include <stdlib.h>
/*
* Napisz program, który wczytuje ze standardowego wejścia trzy liczby
* całkowite i wypisuje na standardowym wyjściu największą z ich wartości
* (pamiętaj o przypadku gdy wszystkie podane liczby lub dwie z nich są równe)
*
* Dawid Sójka
*/

int main()
{
    int a;
    int b;
    int c;

    printf("Podaj pierwsza liczbe:");
    scanf("%d", &a);
    printf("Podaj druga liczbe:");
    scanf("%d", &b);
    printf("Podaj trzecia liczbe:");
    scanf("%d", &c);

    if(a>b && a>c){
        printf("Najwieksza: %d\n", a);
    }
    else if(b>a && b>c){
        printf("Najwieksza: %d\n", b);
    }
    else if(c>a && c>b){
        printf("Najwieksza: %d\n", c);
    }
    else if(a==b && b==c){
        printf("Podano takie same liczby");
    }
    else {
        printf("Cos poszlo nie tak");
    }

    return 0;
}
